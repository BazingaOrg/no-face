/**
 * Search emojis by Chinese or English keywords
 */
export function searchEmojis(query: string): string[] {
  if (!query.trim()) return [];

  const lowerQuery = query.toLowerCase().trim();
  const results: string[] = [];

  // Search through Chinese keywords
  for (const [emoji, keywords] of Object.entries(EMOJI_CHINESE_KEYWORDS)) {
    // Check if any Chinese keyword matches
    if (keywords.some(keyword => keyword.includes(lowerQuery))) {
      results.push(emoji);
    }
  }

  return results;
}
